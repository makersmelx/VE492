from __future__ import print_function
import shop


def shopSmart(orderList, fruitShops):
    """
        orderList: List of (fruit, numPound) tuples
        fruitShops: List of FruitShops
    """
    "*** YOUR CODE HERE ***"
    min_cost = float('inf')
    best_shop = None
    for fruit_shop in fruitShops:
        total_cost = 0
        for pair in orderList:
            fruit = pair[0]
            number = pair[1]
            price = fruit_shop.getCostPerPound(fruit)
            if price is None:
                continue
            total_cost = total_cost + price * number
        if total_cost < min_cost:
            best_shop = fruit_shop
            min_cost = total_cost
    if best_shop is None:
        print("There is no shop to deal with your orders")
    return best_shop


if __name__ == '__main__':
    "This code runs when you invoke the script from the command line"
    orders = [('apples', 1.0), ('oranges', 3.0)]
    dir1 = {'apples': 2.0, 'oranges': 1.0}
    shop1 = shop.FruitShop('shop1', dir1)
    dir2 = {'apples': 1.0, 'oranges': 5.0}
    shop2 = shop.FruitShop('shop2', dir2)
    shops = [shop1, shop2]
    print("For orders ", orders, ", the best shop is", shopSmart(orders, shops).getName())
    orders = [('apples', 3.0)]
    print("For orders: ", orders, ", the best shop is", shopSmart(orders, shops).getName())
